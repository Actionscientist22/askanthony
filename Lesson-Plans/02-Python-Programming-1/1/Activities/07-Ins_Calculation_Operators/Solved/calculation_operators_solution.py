# Create our x and y variables
x = 3
y = 2

# Addition problem
answer = x + y
print("x + y = " + str(answer))

# Subtraction problem
answer = x - y
print("x - y = " + str(answer))

# Multiplication problem
answer = x * y
print("x * y = " + str(answer))

# Division problem
answer = x / y
print("x / y = " + str(answer))

# Modulus problem
answer = x % y
print("x % y = " + str(answer))

# Floor division problem
answer = x // y
print("x // y = " + str(answer))

# Exponent problem
answer = x ** y
print("x ** y (x to the power of y) = " + str(answer))

# Order of operations problem without parenthesis
answer = x + y ** y * x
print("x + y ** y * x = " + str(answer))

# Order of operations problem with parenthesis
answer = (x + y) ** y * x
print("(x + y) ** y * x = " + str(answer))
